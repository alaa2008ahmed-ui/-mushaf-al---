const HisnAlmuslim: React.FC = () => {
    const navigate = useNavigate();
    const [selectedChapter, setSelectedChapter] = useState<Chapter | null>(null);
    const [searchQuery, setSearchQuery] = useState("");
    const [copiedId, setCopiedId] = useState<number | null>(null);

    const filteredChapters = HISN_DATA.filter(chapter => 
        chapter.title.includes(searchQuery) || 
        chapter.adhkar.some(dhikr => dhikr.text.includes(searchQuery))
    );

    const handleCopy = (text: string, id: number) => {
        navigator.clipboard.writeText(text);
        setCopiedId(id);
        setTimeout(() => setCopiedId(null), 2000);
    };

    const handleShare = async (text: string) => {
        if (navigator.share) {
            try {
                await navigator.share({
                    title: 'حصن المسلم',
                    text: text,
                });
            } catch (error) {
                console.error('Error sharing:', error);
            }
        }
    };

    if (selectedChapter) {
        return (
            <div className="min-h-screen bg-gray-50 dark:bg-gray-900 text-gray-900 dark:text-gray-100 flex flex-col font-[var(--font-ui)]">
                <div className="bg-white dark:bg-gray-800 shadow p-4 flex items-center sticky top-0 z-10">
                    <button onClick={() => setSelectedChapter(null)} className="p-2 rounded-full hover:bg-gray-100 dark:hover:bg-gray-700 ml-2">
                        <ArrowRight size={24} />
                    </button>
                    <h1 className="text-xl font-bold flex-1 text-center">{selectedChapter.title}</h1>
                    <div className="w-10"></div>
                </div>

                <div className="flex-1 overflow-y-auto p-4 space-y-4">
                    {selectedChapter.adhkar.map((dhikr) => (
                        <div key={dhikr.id} className="bg-white dark:bg-gray-800 p-6 rounded-2xl shadow-sm border border-gray-100 dark:border-gray-700">
                            <p className="text-xl leading-loose font-[var(--font-hafs)] text-gray-800 dark:text-gray-200 mb-4 text-center">
                                {dhikr.text}
                            </p>
                            
                            {dhikr.note && (
                                <p className="text-sm text-gray-500 dark:text-gray-400 mb-4 bg-gray-50 dark:bg-gray-750 p-3 rounded-lg">
                                    {dhikr.note}
                                </p>
                            )}

                            <div className="flex items-center justify-between mt-6 pt-4 border-t border-gray-100 dark:border-gray-700">
                                <div className="text-sm text-emerald-600 dark:text-emerald-400 font-medium bg-emerald-50 dark:bg-emerald-900/20 px-3 py-1 rounded-full">
                                    {dhikr.reference}
                                </div>
                                <div className="flex items-center gap-2">
                                    <span className="text-sm font-bold text-gray-500 dark:text-gray-400 ml-2">
                                        التكرار: {dhikr.count}
                                    </span>
                                    <button 
                                        onClick={() => handleCopy(dhikr.text, dhikr.id)}
                                        className="p-2 text-gray-400 hover:text-emerald-500 hover:bg-emerald-50 dark:hover:bg-emerald-900/20 rounded-full transition-colors"
                                    >
                                        {copiedId === dhikr.id ? <Check size={20} /> : <Copy size={20} />}
                                    </button>
                                    <button 
                                        onClick={() => handleShare(dhikr.text)}
                                        className="p-2 text-gray-400 hover:text-emerald-500 hover:bg-emerald-50 dark:hover:bg-emerald-900/20 rounded-full transition-colors"
                                    >
                                        <Share2 size={20} />
                                    </button>
                                </div>
                            </div>
                        </div>
                    ))}
                </div>
            </div>
        );
    }

    return (
        <div className="min-h-screen bg-gray-50 dark:bg-gray-900 text-gray-900 dark:text-gray-100 flex flex-col font-[var(--font-ui)]">
            <div className="bg-white dark:bg-gray-800 shadow p-4 flex items-center sticky top-0 z-10">
                <button onClick={() => navigate('/')} className="p-2 rounded-full hover:bg-gray-100 dark:hover:bg-gray-700 ml-2">
                    <ArrowRight size={24} />
                </button>
                <h1 className="text-xl font-bold flex-1 text-center">حصن المسلم</h1>
                <div className="w-10"></div>
            </div>

            <div className="p-4">
                <div className="relative">
                    <input
                        type="text"
                        placeholder="ابحث في الأذكار..."
                        value={searchQuery}
                        onChange={(e) => setSearchQuery(e.target.value)}
                        className="w-full p-3 pr-10 rounded-xl border border-gray-300 dark:border-gray-700 bg-white dark:bg-gray-800 focus:ring-2 focus:ring-emerald-500 outline-none"
                    />
                    <Search className="absolute right-3 top-3.5 text-gray-400" size={20} />
                </div>
            </div>

            <div className="flex-1 overflow-y-auto p-4 pb-24">
                <div className="grid grid-cols-1 gap-3">
                    {filteredChapters.map((chapter) => (
                        <div
                            key={chapter.id}
                            onClick={() => setSelectedChapter(chapter)}
                            className="bg-white dark:bg-gray-800 p-4 rounded-xl shadow-sm hover:shadow-md transition-all cursor-pointer border border-gray-100 dark:border-gray-700 flex items-center justify-between group"
                        >
                            <div className="flex items-center gap-4">
                                <div className="w-10 h-10 rounded-full bg-emerald-100 dark:bg-emerald-900/30 flex items-center justify-center text-emerald-600 dark:text-emerald-400 font-bold text-sm">
                                    {chapter.id}
                                </div>
                                <span className="font-semibold text-lg group-hover:text-emerald-600 dark:group-hover:text-emerald-400 transition-colors">
                                    {chapter.title}
                                </span>
                            </div>
                            <div className="text-gray-400 group-hover:text-emerald-500 group-hover:translate-x-[-4px] transition-all">
                                <ArrowRight size={20} className="rotate-180" />
                            </div>
                        </div>
                    ))}
                </div>
            </div>
        </div>
    );
};

export default HisnAlmuslim;